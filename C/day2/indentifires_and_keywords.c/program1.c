/*
 * Keywords are some words that reserved by the languages. These keywords are not used as identifires.
 * ex: if, else, struct, int, float, ...
 *
 * An identifire is a name that identifies an object like a class, function, struct, variable or constant.
 */

#include <stdio.h>

// int is a keyword
int main()
{

    // value is a identifire
    int value = 10;

    // return is a keyword
    return 0;
}

// void is a keyword
// displayOutput is a identifire
void displayOutput()
{
    printf("Display Something\n");
}