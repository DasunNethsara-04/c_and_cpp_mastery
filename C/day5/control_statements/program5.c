// switch statements
#include <stdio.h>

int main()
{
    int a = 1;

    switch (a)
    {
    case 1:
        printf("1 printed\n");
        break;
    case 2:
        printf("2 printed\n");
        break;
    case 3:
        printf("3 printed\n");
        break;
    case 4:
        printf("4 printed\n");
        break;
    default:
        printf("I dont know!\n");
    }
    return 0;
}